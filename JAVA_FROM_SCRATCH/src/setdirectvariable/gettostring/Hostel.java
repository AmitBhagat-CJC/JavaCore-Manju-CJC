package setdirectvariable.gettostring;

public class Hostel 
{
	long roomno;
	String studentname;
	float studentweight;
	int year;
	
	@Override
	public String toString() 
	{
		return "Hostel [roomno=" + roomno + ", studentname=" + studentname + ", studentweight=" + studentweight
				+ ", year=" + year + "]";
	}
	
}
