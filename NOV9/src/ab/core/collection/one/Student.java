package ab.core.collection.one;

public class Student 
{
	int rollno;
	String name;
		
	public void setRollno (int rollno)
	{
		this.rollno=rollno;
	}
	public int getRollno()
	{
		return rollno;
	}
	
	public void setName(String name)
	{
		this.name=name;
		
	}
	public String getName()
	{
		return name;
	}
}
